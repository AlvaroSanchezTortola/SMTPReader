module Arithmetics
	def sum(num_1, num_2)
		return num_1 + num_2
	end
	def substract(num_1, num_2)
		return num_1 - num_2
	end
	def multiply(num_1, num_2)
		return num_1 * num_2
	end
	def divide(num_1, num_2)
		return num_1 / num_2
	end
end
